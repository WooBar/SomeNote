package com.duanxp.module.attributeforeach;

/**
 * @ClassName: Animal
 * @Description:
 * @Author duanxp
 * @Date 2020/01/07 16:21
 */
public class Animal {

    private String name;

    private int age;

    private double price;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public double getPrice() {
        return price;
    }

    public void setPrice(double price) {
        this.price = price;
    }

}
